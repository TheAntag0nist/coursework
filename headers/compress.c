void create_list(){
    int i;

    for( i = 0; i < SIZE_DICT; ++i)
        list[i] =(byte_node*) malloc(sizeof(byte_node));

    for( i = 1; i < SIZE_DICT - 1; ++i){
        list[i] -> value = i;
        list[i] -> next = list[i + 1];
        list[i] -> previous = list[i - 1];
    }

    list[0] -> previous = NULL;
    list[0] -> next = list[1];
    list[SIZE_DICT - 1] =(byte_node*) malloc(sizeof(byte_node));
    list[SIZE_DICT - 1] -> next = NULL;

    first_elem = list[0];
}
